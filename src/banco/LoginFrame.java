    /**
     * Metodo que se encarga de leer los datos de login de accounts.txt
     */
    public static void getData()
    {
        try{
            File archivo = new File("accounts.txt");
            FileReader fr = new FileReader(archivo);
            BufferedReader br = new BufferedReader(fr);
            String[] partes = new String[2];
            String cadena_leida;
            
            while((cadena_leida = br.readLine())!= null)
            {
                partes = cadena_leida.split("-");
                if(!dataMap.containsKey(partes[0]))
                {
                    dataMap.put(partes[0], partes[1]);
                }
            }
            
            br.close();
            fr.close();
            
        }catch(IOException e)
        {
            System.out.println("Error en lectura");
        }
    }
    /**
     * Metodo que verifica si los datos ingresados son correctos!
     * @param password
     * @param username
     * @return 
     */
    public static boolean verifyInput(JPasswordField password, JTextField username)
    {
       //Rellenamos dataMap
       getData();
       
       String user = username.getText();
       String pass = password.getText();
       
       //Comparamos elementos de dataMap con el input
       for(Map.Entry<String, String> elemento: dataMap.entrySet())
       {
           if( elemento.getKey().equals(user) && elemento.getValue().equals(pass) )
           {
               return true;
           }
       }
       
       return false;
    } 
